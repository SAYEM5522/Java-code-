package LabFinal;

import java.util.Scanner;

public class Multipication {

  public static void main(String[] args) {
    Integer  i;

    for (int j = 1; j <= 5; j++) {
      System.out.println(" \n");

      for (i = 1; i <= 10; i++) {

        System.out.println(j + "x" + i + "=" + (j * i));
      }
    }

  }
}
